function resolveQuestionId(ageGroup: AgeGroup, categoryKey: QuizCategory, index: number) {
  if (ageGroup === '0_1') return legacyQuestionIds0To1[categoryKey][index];
  return `q_${ageGroup}_${questionIdPrefixByCategory[categoryKey]}_${index + 1}`;
}

export const quizCatalog: QuizCatalog = {
  categories: ageGroupOrder.flatMap((ageGroup) =>
    categoryOrder.map((categoryKey, index) => ({
      key: categoryKey,
      ageGroup,
      label: { de: categoryContent[ageGroup][categoryKey].label, ...emptyTranslation },
      description: { de: categoryContent[ageGroup][categoryKey].description, ...emptyTranslation },
      sortOrder: index + 1,
      isActive: true,
    })),
  ),
  questions: ageGroupOrder.flatMap((ageGroup) =>
    categoryOrder.flatMap((categoryKey) =>
      questionTextByAgeGroup[ageGroup][categoryKey].map((text, index) => ({
        id: resolveQuestionId(ageGroup, categoryKey, index),
        ageGroup,
        categoryKey,
        questionText: { de: text, ...emptyTranslation },
        sortOrder: index + 1,
        isActive: true,
      })),
    ),
  ),
};

export const questionTemplates = quizCatalog.questions;
